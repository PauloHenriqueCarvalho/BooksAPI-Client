    private void addTable() {
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setNumRows(0);
        listBook = new ApiBook().fetchBooks();
        for (Book b : listBook) {
            model.addRow(new Object[]{
                b.getName(),
                b.getAutor(),
                b.getGenero()
            });
        }

    }

